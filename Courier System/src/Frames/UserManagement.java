    private void userJTableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_userJTableMouseClicked
        int i=userJTable.getSelectedRow();
        TableModel model=userJTable.getModel();
        nameTextField.setText(model.getValueAt(i, 0).toString());
        emailTextField.setText(model.getValueAt(i, 1).toString());
        phoneNoTextField.setText(model.getValueAt(i, 2).toString());
        passwordPasswordField.setText(model.getValueAt(i, 3).toString());
        if(model.getValueAt(i, 4)==null)
            roleTextField.setText("");
        else
        roleTextField.setText(model.getValueAt(i, 4).toString());

    }//GEN-LAST:event_userJTableMouseClicked

    private void deleteButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteButtonActionPerformed
       DefaultTableModel model =(DefaultTableModel)userJTable.getModel();
       if(userJTable.getSelectedRowCount()==1){
            model.removeRow(userJTable.getSelectedRow());
              
              JOptionPane.showMessageDialog(null, "Deleted Successfully");    
       }
       else
       {
           if(userJTable.getSelectedRowCount()==0){
           JOptionPane.showMessageDialog(null, "Table is empty");
           }
           else{
           JOptionPane.showMessageDialog(null, "Please select single row for delete");
           }
       }
    }//GEN-LAST:event_deleteButtonActionPerformed

    private void updateButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateButtonActionPerformed
       DefaultTableModel model =(DefaultTableModel)userJTable.getModel();
       if(userJTable.getSelectedRowCount()==1){
              String name=nameTextField.getText();
              String email=emailTextField.getText();
              String phone=phoneNoTextField.getText();
              String pass=passwordPasswordField.getText();
              String role=roleTextField.getText();
              
              model.setValueAt(name, userJTable.getSelectedRow(), 0);
              model.setValueAt(email, userJTable.getSelectedRow(), 1);
              model.setValueAt(phone, userJTable.getSelectedRow(), 2);
              model.setValueAt(pass, userJTable.getSelectedRow(), 3);
              model.setValueAt(role, userJTable.getSelectedRow(), 4);
              
              JOptionPane.showMessageDialog(null, "Updated Successfully");    
       }
       else
       {
           if(userJTable.getSelectedRowCount()==0){
           JOptionPane.showMessageDialog(null, "Table is empty");
           }
           else{
           JOptionPane.showMessageDialog(null, "Please select single row for update");
           }
       }
       
    }//GEN-LAST:event_updateButtonActionPerformed

