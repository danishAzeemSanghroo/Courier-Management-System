    private void refresh() throws FileNotFoundException{
  DefaultTableModel model =(DefaultTableModel)userJTable.getModel();
  model.setNumRows(0);        
    String filePath="D:\\Courier System\\Courier System\\src\\TextFiles\\userManagement.txt";
    File file=new File(filePath);
    
              FileReader fr = new FileReader(file);
            BufferedReader br = new BufferedReader(fr);
            
           // DefaultTableModel model = (DefaultTableModel)userJTable.getModel();
            Object[] lines = br.lines().toArray();
            
            for(int i = 0; i < lines.length; i++){
                String[] row = lines[i].toString().split(" ");
                model.addRow(row);
            }
            
    
    }
    private void addUerManagement() {
       
       String name=nameTextField.getText();
       String email=emailTextField.getText();
       String phone=phoneNoTextField.getText();
       String pass=passwordPasswordField.getText();
       String role=roleTextField.getText();
       

       try{
           FileWriter Writer=new FileWriter("D:\\Courier System\\Courier System\\src\\TextFiles\\userManagement.txt",true);
           Writer.write(""+name+" "+email+" "+phone+" "+pass+" "+role);
           Writer.write(System.getProperty("line.separator"));
           Writer.close();
           JOptionPane.showMessageDialog (null,"Successfully Added");
           setVisible(false);
           new UserManagement().setVisible(true);
           refresh();
       }catch(Exception e){
       e.printStackTrace();
       }
       
    }
